def scan(lst):
    if len(lst) == 0:
        return 0

scan([])                 == 0
scan([1, 2, 3, 4])       == 10
scan([2, 3, 4, 5])       == 14
scan([1, 1, 1, 1])       == 1
scan([3, 2, 5, 4])       == 0
scan([4, 3, 2, 1])       == 0
scan([3, 4, 5, 1])       == 0
scan([1, 2, 3, 5, 4])    == 0
scan([1, 2, 3, 3])       == 6
scan([1, 2, 4, 4])       == 7
scan([1, 2, 4, 4, 5])    == 12
scan([-3, -2, -1])       == -6
scan([-1, -2, -3])       == -3
scan([-1, -2, -3, -2, -1]) == -6
scan([3, 2, 1, 2, 3])      == 6
